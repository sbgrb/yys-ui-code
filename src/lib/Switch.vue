<template>
    <button class="yys-switch" @click="toggle" :class="{'yys-checked':value}"><span></span></button>
</template>

<script lang="ts">
    export default {
        props:{
          value:Boolean
        },
        setup(props,context){
            const toggle = () =>{
                context.emit('update:value',!props.value)
            }
            return {toggle}
        }
    }
</script>

